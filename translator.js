// Language selector functionality
class LanguageTranslator {
    constructor() {
        this.currentLanguage = 'en';
        this.init();
    }

    init() {
        this.createLanguageSelector();
        this.bindEvents();
    }

    createLanguageSelector() {
        const selector = document.createElement('div');
        selector.id = 'language-selector';
        selector.innerHTML = `
            <div class="language-dropdown">
                <button class="language-btn" id="current-language">
                    <span class="flag-icon flag-icon-us"></span>
                    English
                    <span class="dropdown-arrow">▼</span>
                </button>
                <div class="language-options" id="language-options">
                    <div class="language-option" data-lang="en">
                        <span class="flag-icon flag-icon-us"></span>
                        English
                    </div>
                    <div class="language-option" data-lang="pt">
                        <span class="flag-icon flag-icon-br"></span>
                        Português
                    </div>
                    <div class="language-option" data-lang="fr">
                        <span class="flag-icon flag-icon-fr"></span>
                        Français
                    </div>
                    <div class="language-option" data-lang="de">
                        <span class="flag-icon flag-icon-de"></span>
                        Deutsch
                    </div>
                    <div class="language-option" data-lang="es">
                        <span class="flag-icon flag-icon-es"></span>
                        Español
                    </div>
                </div>
            </div>
        `;

        // Insert the selector at the top of the page
        document.body.insertBefore(selector, document.body.firstChild);
    }

    bindEvents() {
        const languageBtn = document.getElementById('current-language');
        const languageOptions = document.getElementById('language-options');

        languageBtn.addEventListener('click', () => {
            languageOptions.classList.toggle('show');
        });

        document.addEventListener('click', (e) => {
            if (!e.target.closest('#language-selector')) {
                languageOptions.classList.remove('show');
            }
        });

        document.querySelectorAll('.language-option').forEach(option => {
            option.addEventListener('click', (e) => {
                const selectedLang = e.currentTarget.dataset.lang;
                this.changeLanguage(selectedLang);
                languageOptions.classList.remove('show');
            });
        });
    }

    changeLanguage(lang) {
        if (!translations[lang]) return;

        this.currentLanguage = lang;
        this.updateCurrentLanguageDisplay(lang);
        this.translatePage(lang);
        
        // Store the selected language in localStorage
        localStorage.setItem('selectedLanguage', lang);
    }

    updateCurrentLanguageDisplay(lang) {
        const currentLanguageBtn = document.getElementById('current-language');
        const flagMap = {
            'en': 'us',
            'pt': 'br',
            'fr': 'fr',
            'de': 'de',
            'es': 'es'
        };
        const languageNames = {
            'en': 'English',
            'pt': 'Português',
            'fr': 'Français',
            'de': 'Deutsch',
            'es': 'Español'
        };

        currentLanguageBtn.innerHTML = `
            <span class="flag-icon flag-icon-${flagMap[lang]}"></span>
            ${languageNames[lang]}
            <span class="dropdown-arrow">▼</span>
        `;
    }

    translatePage(lang) {
        const translationData = translations[lang];
        
        // Translate text content
        this.translateTextNodes(document.body, translationData);
        
        // Translate specific attributes
        this.translateAttributes(translationData);
    }

    translateTextNodes(element, translationData) {
        const walker = document.createTreeWalker(
            element,
            NodeFilter.SHOW_TEXT,
            null,
            false
        );

        const textNodes = [];
        let node;
        while (node = walker.nextNode()) {
            textNodes.push(node);
        }

        textNodes.forEach(textNode => {
            const text = textNode.textContent.trim();
            if (text && translationData[text]) {
                textNode.textContent = translationData[text];
            }
        });
    }

    translateAttributes(translationData) {
        // Translate alt attributes
        document.querySelectorAll('img[alt]').forEach(img => {
            const altText = img.getAttribute('alt');
            if (translationData[altText]) {
                img.setAttribute('alt', translationData[altText]);
            }
        });

        // Translate placeholder attributes
        document.querySelectorAll('input[placeholder]').forEach(input => {
            const placeholder = input.getAttribute('placeholder');
            if (translationData[placeholder]) {
                input.setAttribute('placeholder', translationData[placeholder]);
            }
        });

        // Translate title attributes
        document.querySelectorAll('[title]').forEach(element => {
            const title = element.getAttribute('title');
            if (translationData[title]) {
                element.setAttribute('title', translationData[title]);
            }
        });
    }

    // Initialize with saved language or default to English
    loadSavedLanguage() {
        const savedLang = localStorage.getItem('selectedLanguage') || 'en';
        this.changeLanguage(savedLang);
    }
}

// Initialize the translator when the page loads
document.addEventListener('DOMContentLoaded', () => {
    const translator = new LanguageTranslator();
    translator.loadSavedLanguage();
});

